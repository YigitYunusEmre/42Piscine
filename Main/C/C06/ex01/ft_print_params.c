#include <unistd.h>

int	main(int argv, char **argc)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (j < argv)
	{
		while (argc[j][i] != '\0')
		{
			write (1, &argc[j][i], 1);
			i++;
		}
		i = 0;
		write(1, "\n", 1);
		j++;
	}
}
